# for loop with range function

# range(from,to,step)

for i in range(3,11,2): 
    print("Number:", i, "square:", i*i)

# range(fist to count,first not to count)

for i in range(5,9):
    print("i = ", i)

# range(number of counts)

for i in range(999):
    print("you are fuckface #:", i)

# range with float v1

for x in range(18,22):
    print(x/10)

# range with float v2

y = 1.8

for i in range(9):
    y = y + 0.1
    print(y)